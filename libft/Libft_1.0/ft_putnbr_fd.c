#include "libft.h"
#include <unistd.h>
#include <fcntl.h>

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		write(fd, "-", 1);
		write(fd, "2", 1);
		n = 147483648;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		n = n % 10;
	}
	if (n < 10)
	{
		n = n + 48;
		write(fd, &n, 1);
	}
}

int	main(void)
{
	int	fd;

	fd = open("file1.txt", O_RDWR);
	ft_putnbr_fd(-427865, fd);
	close(fd);
	return (0);
}
